#include "ft_ssl.h"

uint32_t	l_rotate32(uint32_t x, uint32_t c)
{
	return (((x) << (c)) | ((x) >> (32 - (c))));
}

uint32_t	r_rotate32(uint32_t x, uint32_t c)
{
	return (((x) >> (c)) | ((x) << (32 - c)));
}

uint32_t	reverse_endian32(uint32_t n)
{
	return ((n >> 24) | ((n & 0xff0000) >> 8) |
		((n & 0xff00) << 8) | (n << 24));
}
